    private void updateAvailableBalance() {
        if (currentAccount == null) return;
        String amountStr = jTextField11.getText().trim();
        if (amountStr.isEmpty()) {
            jTextField12.setText("");
            return;
        }
        try {
            double amount = Double.parseDouble(amountStr);
            if (amount < 0) throw new NumberFormatException();
            double available = currentAccount.balance - amount;
            if (available < 0) {
                jTextField12.setText("Insufficient balance");
            } else {
                jTextField12.setText(String.format("%.2f", available));
            }
        } catch (NumberFormatException e) {
            jTextField12.setText("Invalid amount");
        }
    }
    
    
