    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    if (currentAccount == null) {
        JOptionPane.showMessageDialog(this, "Please search for an account first", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    String amountStr = jTextField11.getText().trim();
    if (amountStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter amount to withdraw", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    double amount;
    try {
        amount = Double.parseDouble(amountStr);
        if (amount <= 0) throw new NumberFormatException();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Enter a valid positive amount", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (currentAccount.age < 18) {
        double limit = 5000.0;
        if (amount > limit) {
            JOptionPane.showMessageDialog(this, 
                "Minor account withdrawal limit is Rs " + limit + " per transaction.\nYour requested amount exceeds this limit.",
                "Limit Exceeded", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    if (amount > currentAccount.balance) {
        JOptionPane.showMessageDialog(this, "Insufficient balance! Available: " + currentAccount.balance, "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    double newBalance = currentAccount.balance - amount;
    boolean updated = BankData.accountList.updateBalance(currentAccount.accountNumber, newBalance);
    if (updated) {
        addTransaction(currentAccount.accountNumber, "Withdraw", amount, null, newBalance);
        JOptionPane.showMessageDialog(this,
            "Withdrawal successful!\nNew Balance: " + String.format("%.2f", newBalance),
            "Success", JOptionPane.INFORMATION_MESSAGE);
        jTextField9.setText(String.valueOf(newBalance));
        currentAccount.balance = newBalance;
        BankData.saveAccountsToFile();
        jTextField11.setText("");
        jTextField12.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Withdrawal failed", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String accNo = jTextField7.getText().trim();
        if (accNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Account Number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BankData.AccountNode acc = BankData.accountList.findActiveByNumber(accNo);
        if (acc == null) {
            JOptionPane.showMessageDialog(this, "Account not found or already closed!", "Error", JOptionPane.ERROR_MESSAGE);
            clearFields();
            currentAccount = null;
            return;
        }
        currentAccount = acc;
        jTextField8.setText(acc.fullName);
        jTextField10.setText(acc.accountType);
        jTextField9.setText(String.valueOf(acc.balance));
        jTextField11.setText("");
        jTextField12.setText("");
        jTextField11.requestFocus();
    }

    private void clearFields() {
        jTextField8.setText("");
        jTextField10.setText("");
        jTextField9.setText("");
        jTextField11.setText("");
        jTextField12.setText("");
    
    }//GEN-LAST:event_jButton3ActionPerformed

private void addTransaction(String accNo, String type, double amount, String relatedAcc, double newBalance) {
    String[] dt = BankData.getCurrentDateTime();
    BankData.TransactionNode trans = new BankData.TransactionNode(dt[0], dt[1], accNo, type, amount, relatedAcc, newBalance);
    BankData.transactionList.add(trans);
    BankData.pushUndoTransaction(trans);
    BankData.saveTransactionsToFile();   // <-- ADD THIS LINE
}
    
