    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
 if (currentAccount == null) {
            JOptionPane.showMessageDialog(this, "Please search for an account first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BankData.TransactionNode lastTrans = getLastTransactionFromStack();
        if (lastTrans == null) {
            JOptionPane.showMessageDialog(this, "No transaction to undo for this account.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (lastTrans.type.startsWith("UNDO_")) {
            JOptionPane.showMessageDialog(this, "Last transaction is already an undo operation. Cannot undo again.", "Error", JOptionPane.ERROR_MESSAGE);
            BankData.pushUndoTransaction(lastTrans);
            return;
        }
        String transType = lastTrans.type;
        double amount = lastTrans.amount;
        String relatedAcc = lastTrans.relatedAccount;
        boolean success = false;
        String undoType = "";
        double newBalance = currentAccount.balance;
        if (transType.equals("Deposit")) {
            if (currentAccount.balance >= amount) {
                newBalance = currentAccount.balance - amount;
                success = BankData.accountList.updateBalance(currentAccount.accountNumber, newBalance);
                undoType = "UNDO_DEPOSIT";
            } else {
                JOptionPane.showMessageDialog(this, "Insufficient balance to undo deposit!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } 
        else if (transType.equals("Withdraw")) {
            newBalance = currentAccount.balance + amount;
            success = BankData.accountList.updateBalance(currentAccount.accountNumber, newBalance);
            undoType = "UNDO_WITHDRAW";
        }
        else if (transType.equals("Transfer Out")) {
            if (relatedAcc == null || relatedAcc.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Invalid transfer record: missing receiver account.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            BankData.AccountNode receiver = BankData.accountList.findActiveByNumber(relatedAcc);
            if (receiver == null) {
                JOptionPane.showMessageDialog(this, "Receiver account not found or closed. Cannot undo transfer.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            newBalance = currentAccount.balance + amount;
            boolean senderOk = BankData.accountList.updateBalance(currentAccount.accountNumber, newBalance);
            double newReceiverBal = receiver.balance - amount;
            if (newReceiverBal < 0) {
                JOptionPane.showMessageDialog(this, "Receiver account would go negative! Cannot undo transfer.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean receiverOk = BankData.accountList.updateBalance(receiver.accountNumber, newReceiverBal);
            if (senderOk && receiverOk) {
                success = true;
                undoType = "UNDO_TRANSFER_OUT";
                addTransaction(receiver.accountNumber, "UNDO_TRANSFER_IN", amount, currentAccount.accountNumber, newReceiverBal);
            }
        }
        else if (transType.equals("Transfer In")) {
            JOptionPane.showMessageDialog(this, "Cannot undo a 'Transfer In' from this account. Use sender's account.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else {
            JOptionPane.showMessageDialog(this, "Cannot undo transaction type: " + transType, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (success) {
            currentAccount.balance = newBalance;
            String[] dt = BankData.getCurrentDateTime();
            BankData.TransactionNode undoTrans = new BankData.TransactionNode(dt[0], dt[1], currentAccount.accountNumber, undoType, amount, relatedAcc, newBalance);
            BankData.transactionList.add(undoTrans);
            BankData.saveAccountsToFile();
            String updatedHistory = BankData.transactionList.getTransactionsForAccount(currentAccount.accountNumber);
            jTextArea1.setText(updatedHistory);
            JOptionPane.showMessageDialog(this, 
                "Undo successful!\nUndone: " + transType + " of Rs." + String.format("%.2f", amount) + 
                "\nNew balance: Rs." + String.format("%.2f", newBalance),
                "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Undo failed. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

