    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String accNo = jTextField1.getText().trim();
        if (accNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Account Number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Check if account exists
        currentAccount = BankData.accountList.findByNumber(accNo);
        if (currentAccount == null) {
            JOptionPane.showMessageDialog(this, "Account not found!", "Error", JOptionPane.ERROR_MESSAGE);
            jTextArea1.setText("");
            return;
        }
        // Check if account is closed
        if (currentAccount.closed) {
            JOptionPane.showMessageDialog(this, "Account is closed! Cannot undo transactions.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextArea1.setText("");
            currentAccount = null;
            return;
        }
        String history = BankData.transactionList.getTransactionsForAccount(accNo);
        jTextArea1.setText(history);
    }//GEN-LAST:event_jButton1ActionPerformed

