    private void startDateTimeUpdater() {
        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                updateDateTime();
            }
        });
        timer.start();
        updateDateTime();
    }

    private void updateDateTime() {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm:ss");
        Date now = new Date();
        jTextField2.setText(dateFormat.format(now));
        jTextField3.setText(timeFormat.format(now));
    }
    
        private void addTransaction(String accNo, String type, double amount, String relatedAcc, double newBalance) {
        String[] dt = BankData.getCurrentDateTime();
        BankData.TransactionNode trans = new BankData.TransactionNode(dt[0], dt[1], accNo, type, amount, relatedAcc, newBalance);
        BankData.transactionList.add(trans);
        // Also push onto undo stack for this account
        BankData.pushUndoTransaction(trans);
    }

            private void clearFields() {
        jTextField1.setText("");
        jTextArea1.setText("");
        currentAccount = null;
    }

    // Helper to get the most recent transaction for this account from the stack
    private BankData.TransactionNode getLastTransactionFromStack() {
        if (currentAccount == null) return null;
        return BankData.popUndoTransaction(currentAccount.accountNumber);
    }

