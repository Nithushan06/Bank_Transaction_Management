    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String fullName = jTextField1.getText().trim();
        String username = jTextField2.getText().trim();
        String dob = jTextField4.getText().trim();        
        String gender = (String) jComboBox1.getSelectedItem();
        String address = jTextField3.getText().trim();
        String contact = jTextField7.getText().trim();
        String email = jTextField8.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();
        String confirm = new String(jPasswordField2.getPassword()).trim();
        
        
        if (fullName.isEmpty() || username.isEmpty() || dob.isEmpty() || address.isEmpty()
                || contact.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!password.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
     
        if (usernames.contains(username)) {
            JOptionPane.showMessageDialog(this, "Username already exists! Please choose another.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        usernames.add(username);
        passwords.add(password);
        saveUserToFile(username, password);
        saveUserDetails(fullName, username, dob, gender, address, contact, email, password);
        JOptionPane.showMessageDialog(this, "Signup Successful! Please login.", "Success", JOptionPane.INFORMATION_MESSAGE);
        new Loginpage().setVisible(true);
        this.dispose(); 
    }//GEN-LAST:event_jButton3ActionPerformed

