// Save a single user to file (append mode)
private static void saveUserToFile(String username, String password) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("users.txt", true))) {
            writer.write(username + "," + password);
            writer.newLine();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error saving user: " + e.getMessage());
        }
    }

// Load all users from file into the static arrays (call this when app starts)
public static void loadUsersFromFile() {
    // Clear existing arrays
    usernames.clear();
        passwords.clear();
        File file = new File("users.txt");
        if (!file.exists()) return;

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 2) {
                    usernames.add(parts[0]);
                    passwords.add(parts[1]);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error loading users: " + e.getMessage());
        }
    }

    private static void saveUserDetails(String fullName, String username, String dob, String gender,
                                        String address, String contact, String email, String password) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("users_details.txt", true))) {
            // Format: fullName,username,dob,gender,address,contact,email,password
            writer.write(fullName + "," + username + "," + dob + "," + gender + ","
                    + address + "," + contact + "," + email + "," + password);
            writer.newLine();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error saving user details: " + e.getMessage());
        }
    }
