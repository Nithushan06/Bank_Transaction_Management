    private boolean updatePasswordInUsersFile(String username, String newPassword) {
        File file = new File("users.txt");
        if (!file.exists()) return false;
        try {
            ArrayList<String> lines = new ArrayList<>();
            BufferedReader reader = new BufferedReader(new FileReader(file));
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 2 && parts[0].equals(username)) {
                    lines.add(username + "," + newPassword);
                } else {
                    lines.add(line);
                }
            }
            reader.close();
            BufferedWriter writer = new BufferedWriter(new FileWriter(file));
            for (String l : lines) {
                writer.write(l);
                writer.newLine();
            }
            writer.close();
            return true;
        } catch (IOException e) {
            return false;
        }
    }

    private boolean updatePasswordInDetailsFile(String username, String newPassword) {
        File file = new File("users_details.txt");
        if (!file.exists()) return false;
        try {
            ArrayList<String> lines = new ArrayList<>();
            BufferedReader reader = new BufferedReader(new FileReader(file));
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 8 && parts[1].equals(username)) {
                    // Keep all fields, only replace password (last part)
                    String newLine = parts[0] + "," + parts[1] + "," + parts[2] + "," + parts[3] + ","
                            + parts[4] + "," + parts[5] + "," + parts[6] + "," + newPassword;
                    lines.add(newLine);
                } else {
                    lines.add(line);
                }
            }
            reader.close();
            BufferedWriter writer = new BufferedWriter(new FileWriter(file));
            for (String l : lines) {
                writer.write(l);
                writer.newLine();
            }
            writer.close();
            return true;
        } catch (IOException e) {
            return false;
        }
    }
