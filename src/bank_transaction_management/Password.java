    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String currentPwd = new String(jPasswordField3.getPassword()).trim();
        if (currentPwd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your current password.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String loggedInUser = Loginpage.loggedInUser;
        if (loggedInUser == null || loggedInUser.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No user logged in!", "Error", JOptionPane.ERROR_MESSAGE);
            new Loginpage().setVisible(true);
            this.dispose();
            return;
        }

        // Find password from users.txt (or Signup.passwords list)
        int index = Signup.usernames.indexOf(loggedInUser);
        if (index != -1 && Signup.passwords.get(index).equals(currentPwd)) {
            isVerified = true;
            jPasswordField2.setEnabled(true);
            jPasswordField1.setEnabled(true);
            jButton2.setEnabled(true);
            jPasswordField3.setEnabled(false);    // disable current password field after verification
            jButton1.setEnabled(false);           // disable Okay button
            JOptionPane.showMessageDialog(this, "Current password verified! You can now set a new password.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Current password is incorrect!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!isVerified) {
            JOptionPane.showMessageDialog(this, "Please verify your current password first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String newPwd = new String(jPasswordField2.getPassword()).trim();
        String confirmPwd = new String(jPasswordField1.getPassword()).trim();

        if (newPwd.isEmpty() || confirmPwd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter and confirm your new password.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!newPwd.equals(confirmPwd)) {
            JOptionPane.showMessageDialog(this, "New password and confirmation do not match!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String loggedInUser = Loginpage.loggedInUser;
        // Update users.txt
        boolean updatedUsers = updatePasswordInUsersFile(loggedInUser, newPwd);
        // Update users_details.txt
        boolean updatedDetails = updatePasswordInDetailsFile(loggedInUser, newPwd);

        if (updatedUsers && updatedDetails) {
            // Also update the in-memory ArrayList in Signup
            int index = Signup.usernames.indexOf(loggedInUser);
            if (index != -1) {
                Signup.passwords.set(index, newPwd);
            }
            JOptionPane.showMessageDialog(this, "Password changed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            new Profile().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update password. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

