         private void updateFinalBalance() {
        if (currentAccount == null) return;
        String amountStr = jTextField3.getText().trim();
        if (amountStr.isEmpty()) {
            jTextField5.setText("");
            return;
        }
        try {
            double amount = Double.parseDouble(amountStr);
            double finalBal = currentAccount.balance + amount;
            jTextField5.setText(String.format("%.2f", finalBal));
        } catch (NumberFormatException e) {
            jTextField5.setText("Invalid amount");
        }
    }
     
     
     
