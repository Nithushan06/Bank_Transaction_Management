    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String accNo = jTextField1.getText().trim();
        if (accNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Account Number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BankData.AccountNode acc = BankData.accountList.findActiveByNumber(accNo);
        if (acc == null) {
            JOptionPane.showMessageDialog(this, "Account not found or already closed!", "Error", JOptionPane.ERROR_MESSAGE);
            clearFields();
            currentAccount = null;
            return;
        }
        currentAccount = acc;
        jTextField2.setText(acc.fullName);
        jTextField6.setText(acc.accountType);
        jTextField4.setText(String.valueOf(acc.balance));
        jTextField3.setText("");
        jTextField5.setText("");
        jTextField3.requestFocus();
    }

    private void clearFields() {
        jTextField2.setText("");
        jTextField6.setText("");
        jTextField4.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
    
        
    
    }//GEN-LAST:event_jButton3ActionPerformed

    
private void addTransaction(String accNo, String type, double amount, String relatedAcc, double newBalance) {
    String[] dt = BankData.getCurrentDateTime();
    BankData.TransactionNode trans = new BankData.TransactionNode(dt[0], dt[1], accNo, type, amount, relatedAcc, newBalance);
    BankData.transactionList.add(trans);
    BankData.pushUndoTransaction(trans);
    BankData.saveTransactionsToFile();   // <-- ADD THIS LINE
}
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (currentAccount == null) {
            JOptionPane.showMessageDialog(this, "Please search for an account first", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String amountStr = jTextField3.getText().trim();
        if (amountStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter amount to deposit", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double amount;
        try {
            amount = Double.parseDouble(amountStr);
            if (amount <= 0) throw new NumberFormatException();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a valid positive amount", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double newBalance = currentAccount.balance + amount;
        boolean updated = BankData.accountList.updateBalance(currentAccount.accountNumber, newBalance);
        if (updated) {
            addTransaction(currentAccount.accountNumber, "Deposit", amount, null, newBalance);
            JOptionPane.showMessageDialog(this,
                "Deposit successful!\nNew Balance: " + String.format("%.2f", newBalance),
                "Success", JOptionPane.INFORMATION_MESSAGE);
            jTextField4.setText(String.valueOf(newBalance));
            currentAccount.balance = newBalance;
            BankData.saveAccountsToFile(); 
            jTextField3.setText("");
            jTextField5.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Deposit failed", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

